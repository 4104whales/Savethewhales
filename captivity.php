              <h3>
                  Keiko’s Early Years
              </h3>

              <p>Keiko was born in 1977 or ’78 in Atlantic waters near Iceland. While an infant, he was stolen from his mother’s side and in 1979 he taken to an Icelandic aquarium called Saedyrasfnid.

In 1982, he was purchased by Marineland in Ontario, Canada. Here he began training and performing for the public. His skin lesions appeared for the first time.

In 1985, Marineland sold him to Reino Aventura, an amusement park in Mexico City, for $350,000. For eight years, he lived in the amusement park in a tank that was much too small and too warm for an orca. These conditions led to his skin lesions becoming worse and his dorsal fin bending over. Bent dorsal fins are often seen in captive orcas due to the lack of exercise.
</p>


              <h3>
                Keiko’s Film Career
              </h3>

              <p>In 1992, the filming of the movie Free Willy began at Reino Aventura in Mexico City.

It was the story of a young boy who befriends a killer whale in his small tank and then coaxes him to leap over a sea wall to freedom. After stardom from Keiko’s role in the film, many negotiations ensued with his Mexican owners to improve his living conditions for Keiko and wanted his screen life of freedom to become his real life. It was a slow process for Keiko to get his freedom he moved to a different Aquarium in Oregon and then one in Iceland to rehabilitate him back into the wild.
</p>



              <h3>
                Keiko Released Into Ocean
              </h3>

              <p>In July 2002, Keiko was released into the open ocean. He was separated from his walk boat and spent several weeks in the waters of the North Atlantic. This was the longest time he had spent away from humans since his capture in 1979. He turned up in Norway, nearly 1,000 miles away. He was a hit in the village of Halsa on Norway’s west coast. So many people wanted to swim with him and even crawl on his back, that animal protection authorities had to place a ban on people approaching him.

Shortly after Keiko release he got pneumonia and it came on quickly. After he died, he was buried on a snowbound pasture. Normally a dead whale would be towed to sea and sunk in deep water. His burial was done in secret to keep the media away. During the night, machines dug a hole near the waterline, and then Keiko was slid a few yards across the snow to his grave. Hundreds of people have visited since and left mementoes on his grave.
</p>

