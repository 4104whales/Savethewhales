Originally housed at Marineland near Los Angeles, Corky and her mate Orky were transferred to Sea World, where Orky only lived for 18 months. In a freak accident, Corky was attacked by a female orca name Kandu, who was killed in the assault. Such a bizarre event has never been witnessed in orca history anywhere in the world.


