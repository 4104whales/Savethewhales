          My name is Maris Sidenstecker II and this is my story. I love animals, and I thought it was just mean to even step on ants or bugs. When I was in first grade, I organized a group of friends to pick up earthworms in the schoolyard after it rained so the boys couldn’t step on them. We always had a lot of pets growing up; I had cats, fish, a turtle, newts, lizards, and a dog. I always wanted to be a veterinarian as a kid.

When I was 14, I took a trip from Los Angeles to Boston. On my flight there I read an article in an airline magazine written by Joan McIntyre, the director of a nonprofit organization called Project Jonah, located in Bolinas, California. The article contained a story about a pregnant blue whale that had suffered on a dock for several days before dying. This story upset me deeply. After reading this article I never realized that whales are mammals just like us humans and they care for their young just like we do. I felt people had to know what was happening to the whales. After when I came home from my trip I designed a T-shirt with a blue whale on it and three simple words: “Save The Whales.” I took $50 out of my saving account and had a dozen shirts printed. I gave the shirts away to friends and family. Soon after people started asking how they could get my shirt. At one point my mother sold some T-shirts through the Rolling Stones magazine and the proceeds went to Project Jonah. Soon after I was asked to appear on local TV programs, animal rights fairs, and other street fairs, selling these shirts. Who knew this shirt would change my life forever?<br><br>

A couple years later when I was 16, Save the Whales became its own non-profit organization. I still lead the organization today with my mother. Later on I attended Humboldt State University in Arcata, California, and study marine biology. After my freshman year, I heard that another student was studying orcas during the summer in Canada and needed assistant. During my trip I learned how to photograph orcas in the wild from a zodiac, record data on the orcas’ behavior, and to record orcas vocalizing underwater. After such an amazing experience I decided I wanted to proceed more research on other types of whales. I graduated from Humboldt State University in 1986 with a double major in marine biology and zoology. Shortly after I started to work at the Catalina Island Marine Institute on Catalina Island off the coast of Southern California. I taught groups of school children from all over about marine life in a hands-on approach.

<br><br>


A couple years later Save The Whales opened an office in Los Angeles, CA. At this time schools were no longer able to provide field trips for students due to budget constrains. Save The Whales developed an innovative hands-on program called “Whales On Wheels” (WOW). The purpose of Save The Whales and WOW is to educate children and adults about marine mammals, their environment, and their preservation. For over 30 years now we have stayed true to our purpose.

What keeps me enthused is interacting with students and the thoughtful letters and questions from students all over the world. I have hope the future generation will protect the planet, the oceans and whales, and that my story will inspire other young people just as the Joan McIntyre article on Project Jonah inspired me.
