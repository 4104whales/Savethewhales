                                SPECIFIC CHARACTERISTICS OF CETACEANS
                            </h3>
                            <p class="accSub">
                            	Diving Capabilities
                                <br>
                                As humans, we can only marvel at the champion swimming and diving capabilities of
                                whales and dolphins. Physiological and anatomical features enable cetaceans to dive
                                for extended lengths of time. Compared to nondiving mammals, cetaceans have a
                                greater ability to transport oxygen across lung membranes, and a higher percentage
                                of oxygen carrying cells in their blood. During prolonged dives, in order to conserve
                                oxygen, their heart rate and peripheral blood flow is reduced. Their muscles contain
                                myoglobin, a substance which combines with oxygen, allowing it to be stored in
