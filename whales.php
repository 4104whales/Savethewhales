                            <h3 class="accH3">
                                Research on Gray Whale Migration:
                            </h3>
                            <p class="accSub">
                            	Each year, gray whales travel some 12,000 miles round trip between their summer
                                feeding areas in the Arctic and their winder breeding and birthing grounds in the
                                lagoons of Baja, California. Typically, the migration is within five miles of the mainland
                                coast of North America. However, as gray whales enter Southern California waters, a
                                significant portion of the population choose offshore routes throughout the Channel
                                Islands instead of a costal route along the mainland shore.
                                <br>
                                <br>
                                Save The Whales and other concerned groups have provided research grants to help
                                define these offshore migration pathways and to determine any changes in these
                                pathways over time. This information is important for effective conservation
                                management of areas where gray whales do travel. Two principal research efforts
                                have taken place.
                                <br>
                                <br>
                                Through the Southern California Area Migration Project (SCAMP), a nonprofit
                                scientific cooperative, observers have been placed on seven of the eight Channel
                                Islands to record daily counts of migrating gray whales. This research, which has
                                been ongoing for seven years, has allowed scientist to estimate the actual number of
                                off shore migrators passing within sight of key offshore observation posts.
                                <br>
                                <br>
                                In addition, Southwest Research Associates (SRA), a SCAMP participant under
                                contract to U.S. Navy, has flown over Southern California waters for three years
                                counting gray whales around and in between the Channel Islands. With these data,
                                the actual migration pathways throughout the Southern California Bight have been
                                defined.
                                <br>
                                <br>
                                To date, these research efforts have produced some conclusions, First, we have
                                confirmed that many gray whales do travel offshore in the Southern California Bight,
                                Second, the actual southbound migration pathways and the relative abundance of
                                gray whales traveling each pathway have been defined. Third, both the islandbased
                                and the airborne data confirm a large offshore increase in the migrating southbound
                                population during the 1989/90 migration. This migration shift occurred during a
                                reverse El Nino and was not permanent.
                                <br>
                                <br>
                                When one does research, more questions are raised than answered. We still do not
                                know what factors caused the offshore shift during the 1989/90 southbound migration.
                                In addition, we have limited knowledge about the more important northbound
                                migration whey gray whale mothers are guiding their calves’ north against the
                                predominant California current. Research is continuing on these question
                                However, with the help of Save The Whales, we have archieved some new basic
                                knowledge about gray whale offshore migration patterns in the Southern California
                                Bight.
