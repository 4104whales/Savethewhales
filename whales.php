                            </h3>
                            <p class="accSub">
								Along the coast of British Columbia, there are three different types of orcas (killer
                                whales). While they are all considered the same species, each group behaves
                                differently. Best known are the Northern resident orcas who are fairly predictable
                                and therefore the most thoroughly researched. Residents have been photo
                                documented, identified and researched extensively over the past 25 years. During the
                                summer, they are regularly seen throughout the small islands off Vancouver Island.
                                The second group is named Transients because you never know when or where
                                these orcas might turn up. Because I work in the winter when transients are often
                                here, I have studied this elusive population. Offshore orcas were just recently
                                discovered and so the least is known about them. As their name implies, they live in
                                the open waters of the Pacific Ocean which makes them difficult to locate and follow.
                                <br>
                                I have never seen an “offshore” orca, but I have worked with the northern resident
                                and transient orcas for 14 years in and around Johnstone strait on the northeastern
                                edge of Vancouver Island. Out of a total population of 450 orcas ranging from
                                Washington state to southeast Alaska, the majority are residents.
                                I have observed that resident and transient orcas exhibit many traits that are nearly
                                opposite in nature.
                            </p>
